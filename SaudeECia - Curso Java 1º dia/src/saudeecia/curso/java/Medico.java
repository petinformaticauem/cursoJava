package saudeecia.curso.java;

public class Medico {
    String nome;
    String cpf;
    String email;
    String dataNasc;
    String endereco;

    public Medico(String nome, String cpf, String email, String dataNasc, String endereco) {
        this.nome = nome;
        this.cpf = cpf;
        this.email = email;
        this.dataNasc = dataNasc;
        this.endereco = endereco;
    }
}
